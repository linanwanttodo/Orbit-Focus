SELECT 
  DATE(start_time) as date,
  COUNT(*) as sessions,
  SUM(duration) as total_seconds,
  ROUND(SUM(duration) / 60.0, 1) as total_minutes
FROM sessions
WHERE id LIKE 'test-%'
GROUP BY DATE(start_time)
ORDER BY date DESC
LIMIT 10;
